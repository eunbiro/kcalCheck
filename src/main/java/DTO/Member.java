package DTO;

public class Member {
	int member_no;
	String mname;
	int height;
	String needKcal;
	
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public String getNeedKcal() {
		return needKcal;
	}
	public void setNeedKcal(String needKcal) {
		this.needKcal = needKcal;
	}
	
	
}
